#!/usr/bin/env node
// MALINDRA PHASE 5
// scripts/generate-sri-hashes.mjs
// Compute Subresource Integrity (SRI) SHA-384 hashes for all static JS/CSS assets.
// Outputs data/sri-manifest.json for use in nginx/CDN CSP headers.

import { createHash } from 'node:crypto';
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { extname, join, relative } from 'node:path';

const OUT_DIR = './out/_next/static';
const MANIFEST_PATH = './data/sri-manifest.json';

function hashFile(filePath) {
  try {
    const content = readFileSync(filePath);
    const hash = createHash('sha384').update(content).digest('base64');
    return `sha384-${hash}`;
  } catch {
    return null;
  }
}

function walkDir(dir, exts = ['.js', '.css']) {
  const results = [];
  if (!existsSync(dir)) return results;
  const entries = readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      results.push(...walkDir(full, exts));
    } else if (exts.includes(extname(e.name))) {
      results.push(full);
    }
  }
  return results;
}

console.log('[sri] Scanning static assets in', OUT_DIR);
const files = walkDir(OUT_DIR);
const manifest = {};
let count = 0;

for (const filePath of files) {
  const sri = hashFile(filePath);
  if (sri) {
    const rel = relative('./out', filePath).replace(/\\/g, '/');
    manifest[`/_next/${rel.replace(/^_next\//, '')}`] = sri;
    count++;
  }
}

const output = {
  generated_at: new Date().toISOString(),
  asset_count: count,
  hashes: manifest,
};

writeFileSync(MANIFEST_PATH, JSON.stringify(output, null, 2), 'utf-8');
console.log(`[sri] Generated ${count} SRI hashes → ${MANIFEST_PATH}`);

// Also output nginx snippet
const nginxLines = [
  '# SRI hash reference for nginx add_header / CSP configuration',
  '# Generated by generate-sri-hashes.mjs',
  `# Assets: ${count}`,
  '',
];

for (const [path, hash] of Object.entries(manifest).slice(0, 5)) {
  nginxLines.push(`# ${path}: ${hash}`);
}
nginxLines.push('# ... (see data/sri-manifest.json for full list)');
writeFileSync('./data/sri-hashes.nginx.conf', nginxLines.join('\n'), 'utf-8');
console.log('[sri] Nginx reference written → data/sri-hashes.nginx.conf');
